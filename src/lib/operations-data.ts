function parseResult(val: string): number {
  const clean = val.replace(/[R$\s]/g, '').replace('.', '').replace(',', '.');
  return parseFloat(clean) || 0;
}

function getStatus(result: number): 'WIN' | 'LOSS' | 'ZERO' {
  if (result > 0) return 'WIN';
  if (result < 0) return 'LOSS';
  return 'ZERO';
}

export function getOperations(): Operation[] {
  return rawData.map(r => {
    const result = parseResult(r.result);
    return {
      date: r.date,
      hour: r.hour.replace(/[hH]/g, 'h').trim() || '-',
      asset: normalizeAsset(r.asset),
      type: (r.type.charAt(0).toUpperCase() + r.type.slice(1).toLowerCase()) as 'Compra' | 'Venda',
      entry: parseResult(r.entry),
      exit: parseResult(r.exit),
      result,
      status: getStatus(result),
    };
  });
}

export function getUniqueAssets(ops: Operation[]): string[] {
  return [...new Set(ops.map(o => o.asset))].sort();
}

export function getUniqueDates(ops: Operation[]): string[] {
  return [...new Set(ops.map(o => o.date))];
}
